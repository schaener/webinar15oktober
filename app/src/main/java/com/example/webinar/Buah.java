package com.example.webinar;

public class Buah {

    int gbrBuah;
    String namaBuah;


    public Buah(int gbrBuah, String namaBuah) {
        this.gbrBuah = gbrBuah;
        this.namaBuah = namaBuah;
    }

    public int getGbrBuah() {
        return gbrBuah;
    }

    public void setGbrBuah(int gbrBuah) {
        this.gbrBuah = gbrBuah;
    }

    public String getNamaBuah() {
        return namaBuah;
    }

    public void setNamaBuah(String namaBuah) {
        this.namaBuah = namaBuah;
    }


}
